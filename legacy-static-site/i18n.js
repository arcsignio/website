// Current language (default to English for global audience)
let currentLang = localStorage.getItem('arcsign-lang') || 'en';

/**
 * Get translation by key
 * @param {string} key - Translation key
 * @returns {string} Translated text
 */
function t(key) {
  return translations[currentLang]?.[key] || translations['en']?.[key] || key;
}

/**
 * Update all elements with data-i18n attribute
 */
function updatePageTranslations() {
  // Update elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);

    // Check if translation contains HTML
    if (translation.includes('<') && translation.includes('>')) {
      el.innerHTML = translation;
    } else {
      el.textContent = translation;
    }
  });

  // Update elements with data-i18n-html (always use innerHTML)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });

  // Update elements with data-i18n-placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });

  // Update document title if needed
  const titleKey = document.querySelector('title')?.getAttribute('data-i18n');
  if (titleKey) {
    document.title = t(titleKey);
  }

  // Update html lang attribute
  document.documentElement.lang = currentLang === 'zh-TW' ? 'zh-TW' : 'en';

  // Update language toggle button text
  const langToggle = document.querySelector('.lang-toggle');
  if (langToggle) {
    langToggle.textContent = currentLang === 'zh-TW' ? 'EN' : '中';
  }
}

/**
 * Switch language
 * @param {string} lang - Language code ('zh-TW' or 'en')
 */
function switchLanguage(lang) {
  if (!translations[lang]) {
    console.warn(`Language ${lang} not found, falling back to en`);
    lang = 'en';
  }

  currentLang = lang;
  localStorage.setItem('arcsign-lang', lang);
  updatePageTranslations();

  // Dispatch custom event for other scripts to react
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

/**
 * Toggle between zh-TW and en
 */
function toggleLanguage() {
  const newLang = currentLang === 'zh-TW' ? 'en' : 'zh-TW';
  switchLanguage(newLang);
}

/**
 * Get current language
 * @returns {string} Current language code
 */
function getCurrentLang() {
  return currentLang;
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  updatePageTranslations();

  // Setup language toggle button click handler
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.addEventListener('click', toggleLanguage);
  });
});

// Export for use in other scripts
window.i18n = {
  t,
  switchLanguage,
  toggleLanguage,
  getCurrentLang,
  updatePageTranslations
};
